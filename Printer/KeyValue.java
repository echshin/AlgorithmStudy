package Printer;

public class KeyValue {
	int idx;
	int value;
	
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	public KeyValue(int idx, int value) {
		super();
		this.idx = idx;
		this.value = value;
	}

	
}
